static int CW_IsInRanges(CW_Node const* ranges, cw_u32 size, cw_c32 c) {
	cw_u32 low = 0;
	cw_u32 high = size;
	cw_u32 mid;
	if (c < ranges[0].low) return 0;
	if (c > ranges[size - 1].high) return 0;
	while (1) {
		mid = ((high - low) / 2) + low;
		if ((c >= ranges[mid].low) && (c <= ranges[mid].high)) return 1;
		if (mid == low) return 0;
		if (c < ranges[mid].low) high = mid;
		else low = mid;
	}
	return 0;
}

static cw_u32 CW_CharWidth(cw_c32 c) {
	if (CW_IsInRanges(cw_kWidth0, 791, c)) return 0;
	if (CW_IsInRanges(cw_kWidth1, 808, c)) return 1;
	if (CW_IsInRanges(cw_kWidth2, 123, c)) return 2;
	return 0;
}